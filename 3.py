a = int(input('a value: '))
b = int(input('b value: '))
summ = a + b
difference = a - b
quotient = a / b
product = a * b
exponent = a ** b
integer_quotient = a // b
modulus = a % b
print('  sum:', summ, '  difference:', difference, '  quotient:', quotient,
      '  product:', product, '  exponent:', exponent, '  integer quotient:', integer_quotient,
      '  modulus:', modulus)
